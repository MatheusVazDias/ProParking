    private void BtnSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BtnSalvarActionPerformed
        {
            Atualizar();

            Locacao locacao = new Locacao();
            Vaga vaga = new Vaga();

            if (txt_Cpf.getText().isEmpty() ) {
                JOptionPane.showMessageDialog(rootPane, "Preencher todos os campos!");
            } else {

                Timestamp Entrada = new Timestamp(System.currentTimeMillis());
                locacao.setCpf(txt_Cpf.getText());
                
                locacao.setPlaca(txt_Placa.getText());
                locacao.setEntrada(Entrada);

                vaga.setStatus(Boolean.parseBoolean("true")); //true = ocupada false = livre
                vaga.setId(locacao.getVaga());

                LocacaoDAO dao = new LocacaoDAO();

                Boolean deucerto = dao.inserir(locacao);

                if (deucerto == true) {

                    vagadao.atualizar(vaga);
                    JOptionPane.showMessageDialog(rootPane, "Você deu entrada na locação!");

                    
                    txt_Id.setText("");
                    txt_Saida.setText("");
                    txt_Cpf.setText("");
                    txt_Entrada.setText("");
                    txt_Placa.setText("");
                } else {
                    JOptionPane.showMessageDialog(rootPane, "Parece que tem algo errado!");
                }

            }
        }
        // TODO add your handling code here:
    }//GEN-LAST:event_BtnSalvarActionPerformed

    private void BtnPesquisarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BtnPesquisarActionPerformed

        Atualizar();

        if (txt_Cpf.getText().isEmpty()) {

            Boolean encontrou = false;
            Integer posicaoachou = 0;

            for (Locacao lista1 : lista) {
                if (txt_Cpf.getText().equals(lista1.getCpf())) {
                    encontrou = true;

                    JOptionPane.showMessageDialog(null, "Exibindo Locações, um momento!");

                    txt_Placa.setText(lista1.getPlaca());
                    txt_Cpf.setText(lista1.getCpf().toString());
                    txt_Id.setText(lista1.getId().toString());
                    txt_Entrada.setText(lista1.getEntrada().toString());
                    txt_Saida.setText(lista1.getSaida().toString());
                    

                    break;
                }

            }

            if (encontrou == false) {
                JOptionPane.showMessageDialog(null, "Locação ainda não cadastrado!");
            }

        } else {
            String cpf = txt_Cpf.getText();
            LocacaoListar locatela;
            locatela = new LocacaoListar(cpf);
            locatela.setVisible(true);
            locatela.setDefaultCloseOperation(DISPOSE_ON_CLOSE);
        }
    }//GEN-LAST:event_BtnPesquisarActionPerformed

    private void BtnExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BtnExcluirActionPerformed
        Atualizar();

        Locacao locacao = new Locacao();
        Vaga vaga = new Vaga();

        if (txt_Placa.getText().isEmpty()==false)
        {
            Timestamp Saida = new Timestamp(System.currentTimeMillis());
            locacao.setCpf(txt_Cpf.getText());
            
            locacao.setPlaca(txt_Placa.getText());
            locacao.setEntrada(Timestamp.valueOf(txt_Entrada.getText()));
            locacao.setSaida(Saida);

            vaga.setStatus(Boolean.parseBoolean("false")); //true = ocupada false = livre

            LocacaoDAO dao = new LocacaoDAO();
            Boolean deucerto = dao.atualizar(locacao);

            if (deucerto == true)
            {
                JOptionPane.showMessageDialog(rootPane, "Registro atualizado com sucesso!");
            }
            else
            {
                JOptionPane.showMessageDialog(rootPane, "Erro ao atualizar registro!");
            }

            lista = dao.listar();
        }
        else
        {
            JOptionPane.showMessageDialog(rootPane, "Registro não informado");
        }
    }//GEN-LAST:event_BtnExcluirActionPerformed

