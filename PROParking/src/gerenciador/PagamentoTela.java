    private void BtnBuscarIdActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BtnBuscarIdActionPerformed
        Atualizar();
        
        
        if (!txt_Id.getText().isEmpty()) {

            Boolean encontrou = false;
            Integer posicaoachou = 0;

            for (Locacao lista2 : lista) {

                if (Integer.parseInt(txt_Id.getText()) == lista2.getId()) {
                    encontrou = true;

                    Limpar();
                    JOptionPane.showMessageDialog(null, "Exibindo Locação, um momento!");

                    txt_Placa.setText(lista2.getPlaca());
                    txt_Cpf.setText(lista2.getCpf().toString());
                    txt_Id.setText(lista2.getId().toString());
                    
                    txt_Entrada.setText(lista2.getEntrada().toString());
                    if (lista2.getSaida() != null) {
                        txt_Saida.setText(lista2.getSaida().toString());
                    }
                    
                Integer Pagar;
                Integer Entrada;
                Integer Saida;
                
                Entrada = Integer.parseInt(lista2.getEntrada().toString());
                Saida = Entrada = Integer.parseInt(lista2.getSaida().toString());
                
                Pagar = (Entrada - Saida) * 4;
                
                    txt_TotalaPg.setText(Pagar.toString());
                    break;
                }

            }

            if (encontrou == false) {
                JOptionPane.showMessageDialog(null, "Locação ainda não cadastrado!");
            }

        }
    }//GEN-LAST:event_BtnBuscarIdActionPerformed

